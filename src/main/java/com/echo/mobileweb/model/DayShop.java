package com.echo.mobileweb.model;

public class DayShop {
    private String shopname;
    private Double sysamount;
    private String remark;
    private String dqname;

    public String getShopname() {
        return shopname;
    }

    public void setShopname(String shopname) {
        this.shopname = shopname;
    }

    public Double getSysamount() {
        return sysamount;
    }

    public void setSysamount(Double sysamount) {
        this.sysamount = sysamount;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getDqname() {
        return dqname;
    }

    public void setDqname(String dqname) {
        this.dqname = dqname;
    }
}
